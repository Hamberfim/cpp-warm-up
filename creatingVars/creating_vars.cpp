#include <iostream>
using namespace std;

int main()
{
    // declare and initialize
    char letter = 'A';
    int iNum = 42;
    float fNum = 7.5;
    double dNum = 3.14159;
    bool isTrue = false;

    cout << "Char: " << letter << endl;
    cout << "Integer: " << iNum << endl;
    cout << "Float: " << fNum << endl;
    cout << "Double: " << dNum << endl;
    cout << "Bool: " << isTrue << endl;

    return 0;
}
